export function retrieveOzwoodKnowledge(query, limit = 4) {
  const text = String(query || '').toLowerCase();
  return OZWOOD_KNOWLEDGE
    .map(entry => {
      let score = 0;
      for (const keyword of entry.keywords) if (text.includes(keyword.toLowerCase())) score += keyword.length > 2 ? 3 : 2;
      for (const topic of entry.topics) {
        if (SYNONYMS[topic]?.some(term => text.includes(term))) score += 2;
      }
      return { ...entry, score };
    })
    .filter(entry => entry.score > 0)
    .sort((a, b) => b.score - a.score)
    .slice(0, limit);
}

export function formatKnowledgeContext(entries) {
  if (!entries.length) return '本轮没有命中 Ozwood 专属资料。不要凭空补充品牌、产品或政策事实。';
  return entries.map((entry, index) =>
    `[K${index + 1}] ${entry.text}\n来源：${entry.source}；核验：${entry.verifiedAt}；${entry.stable ? '相对稳定' : '易变化，回答时要求复核'}`
  ).join('\n\n');
}
